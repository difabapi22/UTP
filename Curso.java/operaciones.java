public class operaciones{
    public static void main(String[] args) {
    int num1=4, num2=8, suma, resta, mult, div;

    suma = num1 + num2;
    resta = num1 - num2;
    mult = num1 * num2;
    div = num1 / num2;
    System.out.println("Resultado suma " + num1 + " + " + num2 + " = " + suma);
    System.out.println("Resultado suma " + num1 + " - " + num2 + " = " + resta);
    System.out.println("Resultado suma " + num1 + " * " + num2 + " = " + mult);
    System.out.println("Resultado suma " + num1 + " / " + num2 + " = " + div);
    }
}   